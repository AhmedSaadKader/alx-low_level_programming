#include "3-calc.h"

/**
 * op_add - adds 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: subtraction result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: multiplication result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}
